# 2D lists = a list of lists

drinks = ['coffe', 'tea', 'water']
dinners = ['pizza', 'hamburger', 'hotdog']
snacks = ['chips', 'popcorn', 'apple']

foods = [drinks, dinners, snacks]
print(foods)# [['coffe', 'tea', 'water'], ['pizza', 'hamburger', 'hotdog'], ['chips', 'popcorn', 'apple']]
print(foods[0])# ['coffe', 'tea', 'water']
print(foods[0][0])# coffe